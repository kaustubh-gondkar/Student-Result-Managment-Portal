    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
                   String classname=(String)jComboBox1.getSelectedItem();
                   String division=(String)jComboBox2.getSelectedItem();
                   String rollno=jTextField1.getText();
                   String name=jTextField2.getText();
                   String gender=(String)jComboBox3.getSelectedItem();
                   
                   try{
                    Class.forName("com.mysql.jdbc.Driver");
                    Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","123456789");
                    Statement st=con.createStatement();
                    st.executeUpdate("insert into student values('"+rollno+"','"+classname+"','"+division+"','"+name+"','"+gender+"')");
                    JOptionPane.showMessageDialog(null, "Data saved Succesfully!");
                    setVisible(false);
                    new adminIndex().setVisible(true);
                   }
                   catch(Exception e){
                    JOptionPane.showMessageDialog(null, e.toString());

                   }
                   
    }//GEN-LAST:event_SaveActionPerformed

