    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
           
        String rollno = jTextField1.getText();
        String english = jTextField3.getText();
        String science = jTextField4.getText();
        String math = jTextField5.getText();
        String history = jTextField6.getText();
        String anc = jTextField7.getText();

        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","123456789");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result values('"+rollno+"','"+english+"','"+science+"','"+math+"','"+history+"','"+anc+"')");
                JOptionPane.showMessageDialog(null,"Data updated successfully");
                setVisible(false);
                new insertResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Roll no does not exist!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }

    }//GEN-LAST:event_SaveActionPerformed

